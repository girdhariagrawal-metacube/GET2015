package assignment2;

import java.util.Date;

import lombok.Data;

@Data
public class Employee {

	private int employeeId;
	private String employeeName;
	private String employeeEmail;
	private Date dateOfBirth;
	private Date dateOfJoining;
	private int ctcPerAnnum;
	
	public String toString() {
		return "Employee : [\nEmployee Id : "+employeeId+"\nEmployee Name :"+employeeName+"\n"
				+"Employee Email:"+employeeEmail+"\nDate Of Birth : "+dateOfBirth+
				"\nDate Of Joining :"+dateOfJoining+"\nCTC Per Annum : "+ctcPerAnnum+"\n]";
	}
	
}
